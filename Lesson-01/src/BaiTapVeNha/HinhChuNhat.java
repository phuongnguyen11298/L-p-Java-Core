package BaiTapVeNha;

public class HinhChuNhat {
    public static void main(String[] args) {
        double height = 17.6d;
        double width = 30.01d;
        double circumference = 2*(height + width);
        double square = height*width;
        System.out.println("Chiều dài hình chữ nhật là "+ width );
        System.out.println("Chiều rộng hình chữ nhật là "+ height);
        System.out.println("Chu vi hình chữ nhật là " + circumference );
        System.out.println("Diện tích hình chữ nhật là " + square);
    }


}
